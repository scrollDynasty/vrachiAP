// Функция для получения списка регионов/городов
export const getRegions = () => {
  return Object.keys(uzbekistanRegions);
};

// Функция для получения районов конкретного региона/города
export const getDistrictsByRegion = (regionName) => {
  const region = uzbekistanRegions[regionName];
  if (!region) return [];
  
  return region.districts;
};

// Функция для поиска района по имени
export const findRegionByDistrict = (districtName) => {
  for (const [regionName, regionData] of Object.entries(uzbekistanRegions)) {
    if (regionData.districts.includes(districtName)) {
      return regionName;
    }
  }
  return null;
};

