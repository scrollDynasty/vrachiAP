// Контекст для языка
export const LanguageContext = React.createContext({
  currentLanguage: 'ru',
  setLanguage: () => {},
  t: (key) => key,
  languages: AVAILABLE_LANGUAGES
});

// Хук для использования переводов
export const useTranslation = () => {
  const context = React.useContext(LanguageContext);
  if (!context) {
    throw new Error('useTranslation must be used within a LanguageProvider');
  }
  return context;
};

// Провайдер языка
export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState('ru');
  
  // Загружаем сохраненный язык при инициализации
  useEffect(() => {
    const savedLanguage = localStorage.getItem('preferred_language');
    if (savedLanguage && AVAILABLE_LANGUAGES.find(lang => lang.code === savedLanguage)) {
      setCurrentLanguage(savedLanguage);
    }
  }, []);
  
  // Функция для смены языка
  const setLanguage = (languageCode) => {
    setCurrentLanguage(languageCode);
    localStorage.setItem('preferred_language', languageCode);
  };
  
  // Функция перевода с защитой от ошибок
  const t = (key) => {
    try {
      if (!key) return '';
      if (!translations || !currentLanguage) return key;
      return translations[currentLanguage]?.[key] || translations.ru?.[key] || key;
    } catch (error) {
      return key || '';
    }
  };
  
  const value = {
    currentLanguage,
    setLanguage,
    t,
    languages: AVAILABLE_LANGUAGES
  };
  
  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

// Компонент селектора языка
